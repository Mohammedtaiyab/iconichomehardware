<?php 
	include 'includes/session.php';

	if(isset($_POST['id'])){
		$id = $_POST['id'];
		
		$conn = $pdo->open();

		$stmt = $conn->prepare("SELECT *,product.ID AS prodid, product.Name AS prodname, categories.Category AS catname FROM product LEFT JOIN categories ON categories.id=product.Categories_id WHERE product.ID=:id");
		$stmt->execute(['id'=>$id]);
		$row = $stmt->fetch();
		
		$pdo->close();

		echo json_encode($row);
	}
?>
